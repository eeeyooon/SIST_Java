package com.test.java; //03.04

public class Ex03_Variable {

	public static void main(String[] args) {
		
		//Ex03_Variable.java
		
		/*
		
		변수, Variable
		: 사용자가 할당받은 메모리의 저장공간. 개발자가 명령어를 사용해서 메모리에 할당받은 공간.
		- 목적 : 원하는 데이터를 읽거나 쓰기 위한 공간
		 변수없이 단순히 데이터만 있으면 이게 어떤건지 의미파악하기 어려움.
		 그래서 변수를 사용하면 그 표현으로 의미를 알 수 있음.
		 
		*/
		
		
		//변수
		
		//1. 생성하기
		//- 자료형 변수명;
		
		byte kor;
		
		//2. 초기화하기
		//- 변수 = 값;
		
		kor = 100;
		
		//3. 사용하기
		
		System.out.println(kor);
		
		
				
		
		
	}
}
